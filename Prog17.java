 // Using Assignment operators
class Prog17{
 public static void main(String[] args)
    {
       
        int a = 10;
        int b = 12;
        System.out.println("a+=b:"+(a+=b));
		System.out.println("a-=b:"+(a-=b));
		System.out.println("a*=b:"+(a*=b));
		System.out.println("a/=b:"+(a/=b));
		System.out.println("a%=b:"+(a%=b));
		
		
       
    }
}